// Get token logo URL
export function getTokenLogoUrl(ticker: string): string | null {
  if (!ticker) return null;
  const normalized = ticker.toUpperCase();
  return TOKEN_LOGOS[normalized] || null;
}

// Get handle avatar URL
export function getHandleIconUrl(handle: string): string | null {
  if (!handle) return null;
  const normalized = handle.replace('@', '').toLowerCase();
  return HANDLE_ICONS[normalized] || null;
}

// Generate placeholder avatar from initials
export function getPlaceholderAvatar(handle: string): string {
  const initial = (handle || 'U').replace('@', '').charAt(0).toUpperCase();
  return `https://ui-avatars.com/api/?name=${initial}&background=2f3336&color=71767b&size=64&bold=true`;
}

// Generate placeholder logo for tickers without explicit mapping
export function getTickerPlaceholder(ticker: string): string {
  const t = (ticker || '?').toUpperCase().slice(0, 4);
  return `https://ui-avatars.com/api/?name=${encodeURIComponent(t)}&background=1f2937&color=9ca3af&size=64&bold=true&length=4`;
}
